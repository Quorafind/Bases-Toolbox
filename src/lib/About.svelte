    </p>
    
    <h2>Features</h2>
    <ul>
      <li><strong>Bases Preview:</strong> Visualize and interact with your Bases data in different views (Table, Calendar, Gallery, Board, Map)</li>
      <li><strong>Dataview Converter:</strong> Convert Dataview queries to Bases format seamlessly</li>
      <li><strong>Live Preview:</strong> See real-time updates as you modify your queries</li>
      <li><strong>Multiple View Types:</strong> Support for various data visualization formats</li>
    </ul>
    
